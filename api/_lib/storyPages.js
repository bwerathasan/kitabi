const THEME_VARIANTS = {
  jungle: JUNGLE,
  space:  SPACE,
  ocean:  OCEAN,
  forest: FOREST,
  desert: DESERT,
  farm:   FARM,
}

/**
 * Returns { title, pages, variant_index, source } for the given order params.
 *
 * Priority:
 *   1. storyJson (from AI True Story Engine) — fully unique, AI-generated
 *   2. Static template variants — deterministic fallback
 *
 * @param {string}      theme
 * @param {string}      childName
 * @param {string}      gender
 * @param {string}      orderId        — used for static variant selection (fallback)
 * @param {object|null} storyJson      — story_json from Supabase (True Story Engine output)
 */
export function getStoryPages(theme, childName, gender, orderId, storyJson = null) {
  // ── Priority 1: AI-generated story ────────────────────────────────────
  if (
    storyJson &&
    Array.isArray(storyJson.pages) &&
    storyJson.pages.length === 16 &&
    storyJson.title
  ) {
    return {
      title:         storyJson.title,
      pages:         storyJson.pages,
      variant_index: -1,             // -1 = AI-generated, not a static variant
      source:        'ai_generated',
    }
  }

  // ── Priority 2: Static template fallback ──────────────────────────────
  const variants = THEME_VARIANTS[theme]
  if (!variants) {
    return { title: '', pages: Array(16).fill(''), variant_index: 0, source: 'fallback_empty' }
  }

  const idx     = selectVariant(orderId, theme, variants.length)
  const variant = variants[idx]
  const he      = gender === 'female' ? 'هي' : 'هو'

  return {
    title:         variant.title,
    pages:         variant.pages(childName, he),
    variant_index: idx,
    source:        'static_template',
  }
}
