/**
 * storyPages.js — Multi-variant story system.
 *
 * Every theme has 3 structurally distinct story variants.
 * Variant is selected deterministically from orderId so:
 *   - same order → same story (consistent PDF / images)
 *   - different orders → usually different story arc
 *
 * getStoryPages(theme, childName, gender, orderId)
 *   → { title, pages: string[16], variant_index }
 */

// ---------------------------------------------------------------------------
// Deterministic variant selector (FNV-1a hash → mod nVariants)
// ---------------------------------------------------------------------------
function selectVariant(orderId, theme, nVariants) {
  let h = 0x811c9dc5
  const str = (orderId || '') + '|' + (theme || '')
  for (let i = 0; i < str.length; i++) {
    h ^= str.charCodeAt(i)
    h = (h * 0x01000193) >>> 0
  }
  return h % nVariants
}

